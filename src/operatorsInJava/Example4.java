package operatorsInJava;

/**
 * This file is all about bitwise operators in java
 *  &. | , ^, ~, , >>>, >>, <<
 */
public class Example4 {
    public static void main(String[] args) {
        int x = -10;
        int y = 10;
        System.out.println(x);
        int c = (~y)+1;
        System.out.println(c);
    }
}
